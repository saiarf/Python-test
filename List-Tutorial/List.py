# Uji Coba List
my_list = ["I","Love","Python","Programming",2017]
print(my_list[0])
print(my_list[4])
print(my_list[-3])

# Uji coba Slising pada list
print(my_list[1:3])
print(my_list[3:])
print(my_list[:4])
print(my_list[-3:-1])

